def sumar(num1 , num2):
    resultado = num1 + num2
    return resultado

def restar(num1 , num2):
    resultado = num1 - num2
    return resultado

def dividir(num1 , num2):
    resultado = num1 / num2
    return resultado

def multiplicar(num1 , num2):
    resultado = num1 * num2
    return resultado

def exponencial(num1 , num2):
    resultado = num1 ** num2
    return resultado

def raizPrimerNumero(num1):
    resultado = num1**(1/2)
    return resultado

def raizSegundoNumero(num2):
    resultado = num2**(1/2)
    return resultado


