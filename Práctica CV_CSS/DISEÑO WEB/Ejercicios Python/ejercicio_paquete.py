def sumar(num1 , num2):
    resultado = num1 + num2
    return resultado

def restar(num1 , num2):
    resultado = num1 - num2
    return resultado

def dividir(num1 , num2):
    resultado = num1 / num2
    return resultado

def multiplicar(num1 , num2):
    resultado = num1 * num2
    return resultado

def nombreCompleto(nombre, pApellido, sApellido):
    n= {pApellido + " "+ sApellido +" " + nombre}
    return n

